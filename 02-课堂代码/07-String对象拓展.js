

// 字符串.startsWith() 
// 作用：匹配参数字符串是否在原字符串的开始位置，返回值为布尔类型。

const url = 'C:/Users/megas/Desktop/02-课堂代码';
const r1 = url.startsWith('C:');
console.log(r1);    // true


// 字符串.endsWith() 
// 作用：匹配参数字符串是否在原字符串的结束位置，返回值为布尔类型。

const url2 = 'C:/Users/megas/Desktop/07-String对象拓展.js';
const r2 = url2.endsWith('.js');
console.log(r2);    // true